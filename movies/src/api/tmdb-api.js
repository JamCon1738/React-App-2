export const getMovies = () => {
  return fetch(
    `http://localhost:8080/api/movies/tmdb/discover`
  ).then((response) => {
    if (!response.ok) {
      throw new Error(response.json().message);
    }
    return response.json();
  })
    .catch((error) => {
      throw error
    });
};

